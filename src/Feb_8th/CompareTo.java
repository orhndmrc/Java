package Feb_8th;

public class CompareTo {
    public static void main(String[] args) {
        // Comparison---> dataType1.compareTo(dataType2)---->the difference of ASCII values of the letters
        String str1= "a";
        String str2 = "c";
        //a=97, c= 99---> 97-99 = -2
        System.out.println(str1.compareTo(str2));
    }
}
