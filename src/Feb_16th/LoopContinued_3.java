package Feb_16th;

public class LoopContinued_3 {
    public static void main(String[] args) {
        for(int i =0; i<=1;i++){
            for(int j =0; j<=1;j++){
                for(int k =0; k<=1;k++){
                    System.out.println(""+i+j+k);
                }
            }

        }
    }
}
