package Feb_12th;

public class NestedLoop {
    public static void main(String[] args) {
         /* Syntax---> for(initialization(i=); condition; incr/decr){
        //code to be executed
        for(initialization(j=); condition; incr/decr){
        }
        }---> We create loops
        */
         for(int i = 0; i<5;i++){
             for(int j = 0; j<6;j++){
                 System.out.println("Hello World");
             }
         }

    }

}
